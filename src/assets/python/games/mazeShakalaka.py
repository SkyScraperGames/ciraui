class EndPoint():
    def __init__(self, x, y):
        self.pos = [x, y]
        self.x = x
        self.y = y
        self.map = [[1, 1], [1, 1]]

    def draw(self):
        x = self.pos[0]
        y = self.pos[1]

        cira.display.putPixel(x, y, 255, 0, 50)
        cira.display.putPixel(x, y + 1, 255, 0, 50)
        cira.display.putPixel(x + 1, y, 255, 0, 50)
        cira.display.putPixel(x + 1, y + 1, 255, 0, 50)


class MyGame(CiraGame):
    # engine variables
    gameTitle = "Maze Shaka-Laka"

    # called once when the program first initializes
    def awake(self):
        print("Welcome to Maze Shaka-Laka!")
        print("Use WASD to get through the maze. Don't touch the walls!")
        self.startPos = (9, 10)
        self.speedRate = .010
        self.map = level1Map
        self.endPoint = EndPoint(5, 6)


    # called at the beginning of each new play session
    def start(self):
        self.player = Player(self)
        self.lastMove = 'UP'

        # Color definitions
        self.borderColor = (0, 255, 0)
        self.headColor = (255, 255, 255)
        self.happyColor = (100, 200, 255)
        self.deathColor = (255, 0, 0)
        self.bodyColor = self.happyColor

        # Speed definitions
        self.speedCounter = 1
        self.speedCountdown = self.speedCounter

        # 10 is slow, 1 is fast. Counter intuitive stuff!
        self.playerSpeedupInterval = 10
        self.playerSpeedupTimer = self.playerSpeedupInterval

        self.deathCountdown = 12
        self.deathTimer = 0

    # called each frame before the scene draws
    def update(self):
        # handle death
        if self.deathTimer > 0:
            self.deathTimer -= 1
            if self.deathTimer <= 0:
                self.reset()
            return

        # handle input
        if cira.keys.getKey('1-Up'):
            self.player.setFace('UP')
        if cira.keys.getKey('1-Right'):
            self.player.setFace('RIGHT')
        if cira.keys.getKey('1-Down'):
            self.player.setFace('DOWN')
        if cira.keys.getKey('1-Left'):
            self.player.setFace('LEFT')

        # player move
        self.speedCountdown -= 1
        if self.speedCountdown <= 0:
            self.lastMove = self.player.moveForward()
            self.speedCountdown = self.playerSpeedupInterval

        # speed things up
        self.playerSpeedupTimer -= 1
        if self.playerSpeedupTimer <= 0:
            self.playerSpeedupInterval -= self.playerSpeedupInterval * self.speedRate
            self.playerSpeedupTimer = self.playerSpeedupInterval

        self.player.isCollision(self.map)

    # called when each new frame needs to be drawn
    def draw(self):
        cira.display.clearScreen(0, 0, 0)

        # draw player
        self.player.draw(self.headColor, self.bodyColor)
        self.endPoint.draw()
        for i in range(len(self.map)):
            for j in range(len(self.map[0])):
                if self.map[i][j] == 1:
                    cira.display.putPixel(j, i, 255, 255, 255)
    # end the game
    def die(self):
        self.deathTimer = self.deathCountdown
        self.bodyColor = self.deathColor
        self.playerSpeedupInterval = 10
        self.playerSpeedupTimer = self.playerSpeedupInterval

    # reset current level
    def resetCurrentLevel(self):
        self.playerSpeedupInterval = 10
        self.playerSpeedupTimer = self.playerSpeedupInterval

    # Load next level!
    def goToNextLevel(self):
        # determine endpoint
        if self.map == level1Map:
            self.map = level2Map
            self.startPos = (1, 0)
            self.speedRate = .0125
        elif self.map == level2Map:
            self.map = level3Map
            self.startPos = (1, 7)
            self.speedRate = .015
        elif self.map == level3Map:
            self.map = level4Map
            self.startPos = (1, 0)
            self.speedRate = .0175
        elif self.map == level4Map:
            self.map = level5Map
            self.startPos = (1, -1)
            self.speedRate = .0175

        if self.map == level1Map:
            self.endPoint = EndPoint(5, 6)
        elif self.map == level2Map:
            self.endPoint = EndPoint(17, 20)
        elif self.map == level3Map:
            self.endPoint = EndPoint(9, 10)
        elif self.map == level4Map:
            self.endPoint = EndPoint(17, 20)
        elif self.map == level5Map:
            self.endPoint = EndPoint(17, 20)

        # Kill the player to restart the level
        self.reset()

class Player():
    def __init__(self, myGame):
        self.myGame = myGame
        self.head = (self.myGame.startPos[0], self.myGame.startPos[1])
        self.face = 'DOWN'
        self.body = []
        self.playerMap = [[1, 1], [1, 1]]

    def reset(self, _map):
        self.head = (self.myGame.startPos[0], self.myGame.startPos[1])
        self.face = 'DOWN'

    # moves the player forward one space, possibly eating a treat

    def moveForward(self):
        self.body.append(self.head)

        if self.face is 'UP':
            if self.head[1] > 0:
                self.head = (self.head[0], self.head[1] - 1)
        elif self.face is 'RIGHT':
            if self.head[0] < cira.display.getWidth() - 1:
                self.head = (self.head[0] + 1, self.head[1])
        elif self.face is 'DOWN':
            if self.head[1] < cira.display.getHeight() - 1:
                self.head = (self.head[0], self.head[1] + 1)
        elif self.face is 'LEFT':
            if self.head[0] > 0:
                self.head = (self.head[0] - 1, self.head[1])
        else:
            raise Exception("Invalid face")

        return self.face

        # returns true if the point is a member of the snake's body

    def isBody(self, point):
        for bodyX, bodyY in self.body:
            if point[0] is bodyX and point[1] is bodyY:
                return True
        return False

        # set the direction of the player

    def setFace(self, face):
        if self.face is face:
            self.moveForward()
        self.face = face

        # draw the player

    def draw(self, headColor, bodyColor):
        for i in range(len(self.playerMap)):
            for j in range(len(self.playerMap[0])):
                cira.display.putPixel(self.head[0] + j, self.head[1] + i, 255, 0, 255)

    def isCollision(self, _map):
        #If colliding with the end, load next level
        if self.head[0] == self.myGame.endPoint.x and self.head[1] == self.myGame.endPoint.y:
            print("Great job!!!")
            self.myGame.goToNextLevel()

        for i in range(len(self.playerMap)):
            for j in range(len(self.playerMap[0])):
                if _map[self.head[1] + i][self.head[0] + j] == 1:
                    self.myGame.die()
